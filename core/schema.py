"""
Модуль суворого контракту схеми Excel-шаблону.
Визначає 27 обов'язкових заголовків Листа 1 ('Реєстр_Даних') та функції перевірки.
"""

TEMPLATE_HEADERS = [
    "Заявка", "заявка", "П.І.Б.", "ІПН", "Дата прийома", "Термін дії СТД",
    "Посада згідно кп", "Випробувальний термін", "Номер телефону", "E-mail",
    "Оклад гросс", "Бонус", "Періодичність бонуса", "Робота",
    "Дата виплата авансу та ЗП", "Графік роботи (з-по)",
    "Графік роботи количество годин", "Кількість дней відпустки",
    "Місто та місце роботи", "Формат роботи", "Матеріальна відповідальність",
    "Інші компенсації", "Коментарі", "Реквізити картки",
    "Функціональні обов'язки", "Примітка", "документи",
]

HEADER_TO_COL = {header: index + 1 for index, header in enumerate(TEMPLATE_HEADERS)}


def validate_template_headers(ws) -> None:
    """
    Перевіряє, що Заголовки Рядка 1 повністю та посимвольно відповідають суворому контракту TEMPLATE_HEADERS.
    Видаляє будь-які додаткові порожні колонки понад 27 для гарантії max_column == 27.
    """
    actual = [ws.cell(row=1, column=i).value for i in range(1, len(TEMPLATE_HEADERS) + 1)]
    if actual != TEMPLATE_HEADERS:
        raise ValueError(
            "Порушено суворий контракт шаблону Excel: заголовки або їхній порядок не збігаються. "
            f"Очікувалося: {TEMPLATE_HEADERS}, отримано: {actual}"
        )

    # Забезпечує відсутність залишкових колонок після 27
    if ws.max_column > len(TEMPLATE_HEADERS):
        ws.delete_cols(len(TEMPLATE_HEADERS) + 1, ws.max_column - len(TEMPLATE_HEADERS))
